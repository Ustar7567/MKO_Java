package f_StringClassinJava;

public class A_startsWithAndEndsWith {

	public static void main(String[] args) {
		String str="Selenium Training in Bangalore";
		boolean status=str.startsWith("Selenium");
		System.out.println(status);
		boolean status1=str.endsWith("Bangalore");
		System.out.println(status1);
				

	}

}
