package a_VariablesAndDataTypes;

public class A_Boolean {
	
	public static void main(String[] args) {
		boolean status;//Variable declaration
		status=true;//Variable initialization
		boolean newStatus=false;//Declaration and Initialization in the same line *Highly recommended procedure
		System.out.println("Old status was "+status);
		System.out.println("New status is "+newStatus);

	}

}
